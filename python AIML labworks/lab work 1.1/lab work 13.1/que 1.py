import datetime

now = datetime.datetime.now()

print("Current Date & Time:", now)
print("Year:", now.year)
print("Month:", now.month)
print("Day:", now.day)
print("Hour:", now.hour)
print("Minute:", now.minute)
print("Second:", now.second)

# output:-

# Current Date & Time: 2025-12-28 11:59:42.710540
# Year: 2025
# Month: 12
# Day: 28
# Hour: 11
# Minute: 59
# Second: 42
