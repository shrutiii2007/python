# 3. Search, Sort, Aggregating/Statistical Functions

import numpy as np
arr = np.array([10,20,30,40,50])
print(arr)

print("maximum from the array:-",np.max(arr))    
print("index number of the maximum:-",np.argmax(arr))

print("minimum from the array:-",np.min(arr))
print("index number of the minimum:-",np.argmin(arr))

#output:-

# [10 20 30 40 50]
# maximum from the array:- 50
# index number of the maximum:- 4
# minimum from the array:- 10
# index number of the minimum:- 0



